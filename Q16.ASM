; PROGRAM TO ADD VALUE OF 2 ARRAYS
	AREA PROGRAM, CODE,READONLY
	ENTRY
MAIN
	LDR R0,=VAL1
	LDR R1,=VAL2
	LDR R2,VAL3 ; STORING THE RESULT
	LDR R3,COUNT ;SIZE OF ARRAYS
LOOP
	LDRB R4,[R0],#0x01 ;TRAVERSING ON ARRAY 1
	LDRB R5,[R1],#0x01 ; TRAVERSING ON ARRAY 2
	ADD R8,R4,R5 ; ADDING THE ELEMENT OF ARRAY 1 & 2 AND STORING ON R8
	STRB R8,[R2],#0x01 ;STORING THE VALUE AT R8 TO THE LOCATION STORED IN R2 i.e, LOCATION OF VAL3
	SUB R3,#0x01 ;DECREMENTING THE COUNTER
	CMP R3,#0x00 ;COMPARING THE NO. OF ELEMENTS LEFT WITH 0
	BNE LOOP
	AREA PROGRAM,DATA,READONLY
COUNT DCD &00000005
VAL1 DCB 1,2,3,4,5
VAL2 DCB 6,7,8,9,10
VAL3 DCD &00000090
	END
