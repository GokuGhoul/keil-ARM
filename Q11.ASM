	AREA PROGRAM,CODE,READONLY
ENTRY
	LDR R0,=ARY
	LDR R1,COUNT
	LDRB R3,[R0] ;REGISTER FOR GREATEST ELEMENT
	LDRB R4,[R0] ;REGISTER FOR SMALLEST ELEMENT
LOOP
	LDRB R2,[R0],#0X01
	CMP R2,R3
	MOVGT R3,R2
	CMP R2,R4
	MOVLT R4,R2
	SUB R1,#0X01
	CMP R1,0X00
	BNE LOOP
	AREA PROGRAM,DATA,READONLY
COUNT DCD 9
ARY DCB 2,4,6,8,1,3,5,6,9
	END
